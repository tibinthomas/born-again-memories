enum WhoMetric { weight, height, head }

/// Returns [p3, p50, p97] for the given metric/gender at [ageMonths] (0–60).
/// Returns null when ageMonths is outside the 0–60 range.
List<double>? whoPercentiles({
  required String gender, // 'boy', 'girl', or anything else → averaged
  required WhoMetric metric,
  required int ageMonths,
}) {
  if (ageMonths < 0 || ageMonths > 60) return null;

  final boyTable = switch (metric) {
    WhoMetric.weight => _weightBoy,
    WhoMetric.height => _heightBoy,
    WhoMetric.head   => _headBoy,
  };
  final girlTable = switch (metric) {
    WhoMetric.weight => _weightGirl,
    WhoMetric.height => _heightGirl,
    WhoMetric.head   => _headGirl,
  };

  if (gender == 'boy') return boyTable[ageMonths];
  if (gender == 'girl') return girlTable[ageMonths];

  // neutral → average of boy and girl
  final b = boyTable[ageMonths];
  final g = girlTable[ageMonths];
  return [(b[0] + g[0]) / 2, (b[1] + g[1]) / 2, (b[2] + g[2]) / 2];
}

/// Estimates the approximate percentile (0–100) for a given value.
/// Uses linear interpolation between the 3 reference points.
double estimatePercentile(List<double> pct, double value) {
  final p3 = pct[0], p50 = pct[1], p97 = pct[2];
  if (value <= p3) return 3.0 * (value / p3).clamp(0.0, 1.0);
  if (value <= p50) return 3.0 + 47.0 * ((value - p3) / (p50 - p3));
  if (value <= p97) return 50.0 + 47.0 * ((value - p50) / (p97 - p50));
  return 97.0 + 3.0 * ((value - p97) / p97).clamp(0.0, 1.0);
}
